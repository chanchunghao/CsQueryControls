namespace CsQueryControls.HtmlAttributes {
    /// <summary>
    ///     Specifies the shape of a link
    /// </summary>
    public enum Shape {
        /// <summary>
        ///     Specifies the entire region
        /// </summary>
        Default,
        /// <summary>
        ///     Defines a rectangular region
        /// </summary>
        Rect,
        /// <summary>
        ///     Defines a circular region
        /// </summary>
        Circle,
        /// <summary>
        ///     Defines a polygonal region
        /// </summary>
        Poly
    }
}